package com.iflytek.staff.chao.structure.base.list;

import java.util.*;

/**
 * @author : hamilton
 * @Description: 链表 算法
 * @date Date : 2022年06月23日 下午3:49
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }



}
